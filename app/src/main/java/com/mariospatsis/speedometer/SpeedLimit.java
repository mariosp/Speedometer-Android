package com.mariospatsis.speedometer;

public class SpeedLimit {
    private int id;
    private String slimit;

    public SpeedLimit() {
    }

    public int getId() {
        return id;
    }

    public String getSlimit() {
        return slimit;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setSlimit(String slimit) {
        this.slimit = slimit;
    }
}
